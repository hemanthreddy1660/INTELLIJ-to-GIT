import java.util.*;
import java.lang.*;
import java.io.*;

class Codechef
{
    public static void main (String[] args) throws java.lang.Exception
    {
        Scanner sc = new Scanner(System.in);
        int tc = sc.nextInt();
        for(int i=0;i<=tc;i++){
            int a = sc.nextInt();
            int b = sc.nextInt();
            System.out.println(Math.abs(a-b));
        }

    }
}
